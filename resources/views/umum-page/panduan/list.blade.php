                <strong>Definisi Hak Cipta</strong> <br>
                Hak Cipta merupakan salah satu bagian dari kekayaan intelektual yang memiliki ruang lingkup
                objek dilindungi paling luas, karena mencakup ilmu pengetahuan, seni dan sastra (art and
                literary) yang di dalamnya mencakup pula program komputer. Perkembangan ekonomi kreatif yang
                menjadi salah satu andalan Indonesia dan berbagai negara dan berkembang pesatnya teknologi
                informasi dan komunikasi mengharuskan adanya pembaruan Undang-Undang Hak Cipta, mengingat Hak
                Cipta menjadi basis terpenting dari ekonomi kreatif nasional. Dengan Undang-Undang Hak Cipta
                yang memenuhi unsur pelindungan dan pengembangan ekonomi kreatif ini maka diharapkan kontribusi
                sektor Hak Cipta dan Hak Terkait bagi perekonomian negara dapat lebih optimal.
            </div>
            <div class="accordion-body text-start">
                <strong>Ciptaan Yang Dapat Dilindungi</strong> <br>
                <ol>
                    <li>
                        Buku, program komputer, pamflet, perwajahan (layout) karya tulis yang diterbitkan, dan semua
                        hasil karya tulis lain;
                    </li>
                    <li>
                        Ceramah, kuliah, pidato, dan ciptaan lain yang sejenis dengan itu;
                    </li>
                    <li>
                        Alat peraga yang dibuat untuk kepentingan pendidikan dan ilmu pengetahuan;
                    </li>
                    <li>
                        Lagu atau musik dengan atau tanpa teks;
                    </li>
                    <li>
                        Drama atau drama musikal, tari, koreografi, pewayangan, dan pantomim;
                    </li>
                    <li>
                        Seni rupa dalam segala bentuk seperti seni lukis, gambar, seni ukir, seni kaligrafi, seni pahat,
                        seni patung, kolase, dan seni terapan;
                    </li>
                    <li>
                        Arsitektur;
                    </li>
                    <li>
                        Peta;
                    </li>
                    <li>
                        Seni Batik;
                    </li>
                    <li>
                        Fotografi;
                    </li>
                    <li>
                        Terjemahan, tafsir, saduran, bunga rampai, dan karya lain dari hasil pengalihwujudan.
                    </li>
                </ol>
            </div>
