        <div id="collapseOne" class="accordion-collapse collapse" data-bs-parent="#accordionExample"> {{-- Paten --}} 
            <div class="accordion-body text-start">
                <strong>Definisi Paten</strong> <br>
                Paten adalah hak eksklusif inventor atas invensi di bidang teknologi untuk selama waktu tertentu
                melaksanakan sendiri atau memberikan persetujuan kepada pihak lain untuk melaksanakan
                invensinya.
            </div>
            <div class="accordion-body text-start">
                <strong>Pengertian Invensi</strong> <br>
                Invensi adalah ide inventor yang dituangkan ke dalam suatu kegiatan pemecahan masalah yang
                spesifik di bidang teknologi, dapat berupa produk atau proses atau penyempurnaan dan
                pengembangan produk atau proses.
            </div>
            <div class="accordion-body text-start">
                <strong>Definisi Paten Sederhana</strong> <br>
                Setiap invensi berupa produk atau alat yang baru dan mempunyai nilai kegunaan praktis disebabkan
                karena bentuk, konfigurasi, konstruksi atau komponennya dapat memperoleh perlindungan hukum
                dalam bentuk paten sederhana.
            </div>
            <div class="accordion-body text-start">
                <strong>Perbedaan Paten dan Paten Sederhana</strong> <br>
                <ol>
                    <li>
                        Paten diberikan untuk invensi yang baru, mengandung langkah inventif, dan dapat
                        diterapkan dalam industri. Sementara paten sederhana diberikan untuk setiap invensi
                        baru, pengembangan dari produk atau proses yang telah ada, dan dapat diterapkan dalam
                        industri. Paten sederhana diberikan untuk invensi yang berupa produk yang bukan sekadar
                        berbeda ciri teknisnya, tetapi harus memiliki fungsi/kegunaan yang lebih praktis
                        daripada invensi sebelumnya yang disebabkan bentuk, konfigurasi, konstruksi, atau
                        komponennya yang mencakup alat, barang, mesin, komposisi, formula, senyawa, atau sistem.
                        Paten sederhana juga diberikan untuk invensi yang berupa proses atau metode yang baru.;
                    </li>
                    <li>
                        Klaim paten sederhana dibatasi dengan satu klaim mandiri, sedangkan paten jumlah
                        klaimnya tidak dibatasi.;
                    </li>
                    <li>
                        Progres teknologi dalam paten sederhana lebih simpel daripada progres teknologi dalam
                        paten.
                    </li>
                </ol>
            </div>
            <div class="accordion-body text-start">
                <strong>Invensi dapat dipatenkan jika invensi tersebut</strong> <br>
                <ol>
                    <li>
                        Baru. Jika pada saat pengajuan permohonan Paten invensi tersebut tidak sama dengan
                        teknologi yang diungkapkan sebelumnya;
                    </li>
                    <li>
                        Mengandung langkah inventif. Jika invensi tersebut merupakan hal yang tidak dapat diduga
                        sebelumnya bagi seseorang yang mempunyai keahlian tertentu di bidang teknik;
                    </li>
                    <li>
                        Dapat diterapkan dalam industri. Jika invensi tersebut dapat diproduksi atau dapat
                        digunakan dalam berbagai jenis industri.
                    </li>
                </ol>
            </div>
            <div class="accordion-body text-start">
                <strong>Masa Pelindungan Paten</strong> <br>
                <ol>
                    <li>
                        Paten diberikan untuk jangka waktu selama 20 tahun sejak tanggal penerimaan permohonan
                        Paten.
                    </li>
                    <li>
                        Paten sederhana diberikan untuk jangka waktu 10 tahun sejak tanggal penerimaan
                        permohonan Paten sederhana.
                    </li>
                </ol>
            </div>
